<script>
    // =============================================
    function randInt(min, max) { return Math.floor(Math.random() * (max - min + 1)) + min; }
    function randFloat(min, max) { return (Math.random() * (max - min) + min).toFixed(1); }
    var streamTemplates = [
        function() { return 'SYS.CHECK > OK [' + randFloat(0, 100) + 'ms]'; },
        function() { return 'RECV :: PKT_' + String(randInt(0, 999)).padStart(3, '0'); },
        function() { return 'LINK.STATUS = ACTIVE'; },
        function() { return 'MEM_ALLOC ' + randInt(0, 512) + 'MB'; },
        function() { return 'SYNC.NODE >> ' + randInt(0, 16) + '.' + randInt(0, 255); },
        function() { return 'PROC.ID: ' + Math.random().toString(16).slice(2, 10).toUpperCase(); },
        function() { return 'TEMP: ' + randFloat(17, 22) + '\u00B0C'; },
        function() { return 'I/O BUFFER: ' + randInt(0, 100) + '%'; },
    ];

    function genLine() {
        return streamTemplates[randInt(0, streamTemplates.length - 1)]();
    }

    var streamLines = [];
    for (var s = 0; s < 12; s++) { streamLines.push(genLine()); }

    function renderStream() {
        var el = document.getElementById('dataStream');
        if (!el) return;
        el.innerHTML = '';
        streamLines.forEach(function(line, i) {
            var div = document.createElement('div');
            div.className = 'stream-line';
            var brightness = 30 + i * 3;
            var sat = 40 + i * 3;
            if (i === streamLines.length - 1) {
                div.style.color = 'hsl(185, 100%, 70%)';
            } else {
                div.style.color = 'hsl(185, ' + sat + '%, ' + brightness + '%)';
            }
            var num = document.createElement('span');
            num.className = 'stream-num';
            num.textContent = String(i + 1).padStart(2, '0') + ':';
            div.appendChild(num);
            div.appendChild(document.createTextNode(line));
            el.appendChild(div);
        });
    }
    renderStream();
    setInterval(function() {
        streamLines.shift();
        streamLines.push(genLine());
        renderStream();
    }, 1500);

</script>


